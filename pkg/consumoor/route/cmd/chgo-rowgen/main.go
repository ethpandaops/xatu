	goTypeBool    = "bool"
	goTypeInt8    = "int8"
	goTypeInt16   = "int16"
	goTypeInt32   = "int32"
	goTypeInt64   = "int64"
	goTypeUint8   = "uint8"
	goTypeUint16  = "uint16"
	goTypeUint32  = "uint32"
	goTypeUint64  = "uint64"
	goTypeFloat32 = "float32"
	goTypeFloat64 = "float64"

	chTypeUInt8          = "UInt8"
