	chTypeEnum8          = "Enum8"
	chTypeEnum16         = "Enum16"
	chTypeFixedString    = "FixedString"
	chTypeIPv4           = "IPv4"
	chTypeIPv6           = "IPv6"
	chTypeString         = "String"
